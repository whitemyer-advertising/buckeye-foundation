				<p>Michael Goodwin is a 1979 graduate of New Philadelphia High School and the Carpentry program at Buckeye Career Center. Under the guidance of Mr. Gasser and Mr. Huebner, Michael learned aspects in the field of carpentry. The Class of 1979 was the first Class to build a Buckeye house, a tradition which continues to this day. After graduation, Michael worked in the construction field, eventually taking a position with an HVAC company working as an installer. In 1983, Michael returned to Buckeye to pursue training in Law Enforcement. After graduating from the Ohio Peace Officer Training Academy, Michael gained experience at the Tuscarawas County Sheriff Department while still working in construction. In 1990, Michael accepted a full time position with the New Philadelphia Police Department. During his career with N.P.P.D. Michael worked as a Patrol Officer for ten years before being promoted to Captain. After a short time as Patrol Captain, Michael was appointed to Detective Captain where he was in charge of the Detective Bureau for twelve years with a focus on crimes against children. In March of 2013, Michael was promoted to Chief of Police. Michael is currently on the advisory board for the Law Enforcement class at Buckeye along with being on the executive board of the Ohio Association of Chiefs of Police.</p>
